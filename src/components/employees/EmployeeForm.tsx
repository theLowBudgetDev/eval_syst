
"use client";

import * as React from "react";
import { useForm, Controller } from "react-hook-form";
import { zodResolver } from "@hookform/resolvers/zod";
import * as z from "zod";
import { Button } from "@/components/ui/button";
import { Input } from "@/components/ui/input";
import { Label } from "@/components/ui/label";
import {
  Dialog,
  DialogContent,
  DialogHeader,
  DialogTitle,
  DialogDescription,
  DialogFooter,
} from "@/components/ui/dialog";
import {
  Select,
  SelectContent,
  SelectItem,
  SelectTrigger,
  SelectValue,
} from "@/components/ui/select";
import type { AppUser, UserRoleType } from "@/types"; // Updated import

const NO_SUPERVISOR_VALUE = "--NONE--";
const USER_ROLES: UserRoleType[] = ['ADMIN', 'SUPERVISOR', 'EMPLOYEE'];

// Schema should align with AppUser and what the API expects
const employeeSchema = z.object({
  name: z.string().min(2, "Name must be at least 2 characters"),
  email: z.string().email("Invalid email address"),
  department: z.string().min(1, "Department is required"),
  position: z.string().min(1, "Position is required"),
  hireDate: z.string().regex(/^\d{4}-\d{2}-\d{2}$/, "Invalid date format (YYYY-MM-DD)"),
  role: z.enum(USER_ROLES, { errorMap: () => ({ message: "Invalid role" }) }),
  supervisorId: z.string().optional(), 
  avatarUrl: z.string().url("Invalid URL for avatar").optional().or(z.literal('')),
});

type EmployeeFormData = z.infer<typeof employeeSchema>;

interface EmployeeFormProps {
  isOpen: boolean;
  setIsOpen: (isOpen: boolean) => void;
  employee?: AppUser | null;
  onSubmit: (data: AppUser) => void; // onSubmit now expects full AppUser
  supervisors: AppUser[]; // Supervisors are also AppUser objects
  canEditAllFields?: boolean;
}

export function EmployeeForm({
  isOpen,
  setIsOpen,
  employee,
  onSubmit,
  supervisors,
  canEditAllFields = false,
}: EmployeeFormProps) {
  const {
    control,
    register,
    handleSubmit,
    reset,
    formState: { errors },
    setValue,
  } = useForm<EmployeeFormData>({
    resolver: zodResolver(employeeSchema),
    defaultValues: {
      name: "",
      email: "",
      department: "",
      position: "",
      hireDate: new Date().toISOString().split('T')[0],
      role: "EMPLOYEE", // Default role for new employees
      supervisorId: "", 
      avatarUrl: "",
    },
  });

  React.useEffect(() => {
    if (isOpen) { 
      if (employee) {
        reset({
          name: employee.name,
          email: employee.email,
          department: employee.department,
          position: employee.position,
          hireDate: employee.hireDate.split('T')[0], // Ensure only date part
          role: employee.role,
          supervisorId: employee.supervisorId || "", 
          avatarUrl: employee.avatarUrl || "",
        });
      } else {
        reset({
          name: "",
          email: "",
          department: "",
          position: "",
          hireDate: new Date().toISOString().split('T')[0],
          role: "EMPLOYEE",
          supervisorId: "",
          avatarUrl: "",
        });
      }
    }
  }, [employee, reset, isOpen]);

  const handleFormInternalSubmit = (data: EmployeeFormData) => {
    // Construct the AppUser object to pass to the parent onSubmit
    const submissionData: AppUser = {
      id: employee?.id || "", // ID will be generated by backend for new, or use existing for update
      name: data.name,
      email: data.email,
      department: data.department,
      position: data.position,
      hireDate: data.hireDate, // Already string YYYY-MM-DD
      role: data.role,
      avatarUrl: data.avatarUrl || null,
      supervisorId: data.supervisorId === NO_SUPERVISOR_VALUE || data.supervisorId === "" ? null : data.supervisorId,
      // supervisor and supervisedEmployees will be handled by API/parent
    };
    onSubmit(submissionData);
  };

  const isReadOnlyCoreFields = !canEditAllFields && !!employee; // Core fields read-only if not admin and editing
  const isRoleReadOnly = !canEditAllFields; // Role field read-only if not admin

  return (
    <Dialog open={isOpen} onOpenChange={setIsOpen}>
      <DialogContent className="sm:max-w-[425px] md:sm:max-w-[600px]">
        <DialogHeader>
          <DialogTitle>{employee ? "Edit Employee" : "Add New Employee"}</DialogTitle>
          <DialogDescription>
            {employee ? "Update the employee's details." : "Fill in the details for the new employee."}
          </DialogDescription>
        </DialogHeader>
        <form onSubmit={handleSubmit(handleFormInternalSubmit)} className="grid gap-4 py-4">
          <div className="grid grid-cols-1 md:grid-cols-2 gap-4">
            <div>
              <Label htmlFor="name">Full Name</Label>
              <Input id="name" {...register("name")} className="mt-1" readOnly={isReadOnlyCoreFields} />
              {errors.name && <p className="text-sm text-destructive mt-1">{errors.name.message}</p>}
            </div>
            <div>
              <Label htmlFor="email">Email Address</Label>
              <Input id="email" type="email" {...register("email")} className="mt-1" readOnly={isReadOnlyCoreFields}/>
              {errors.email && <p className="text-sm text-destructive mt-1">{errors.email.message}</p>}
            </div>
          </div>

          <div className="grid grid-cols-1 md:grid-cols-2 gap-4">
            <div>
              <Label htmlFor="department">Department</Label>
              <Input id="department" {...register("department")} className="mt-1" readOnly={isReadOnlyCoreFields} />
              {errors.department && <p className="text-sm text-destructive mt-1">{errors.department.message}</p>}
            </div>
            <div>
              <Label htmlFor="position">Position</Label>
              <Input id="position" {...register("position")} className="mt-1" readOnly={isReadOnlyCoreFields} />
              {errors.position && <p className="text-sm text-destructive mt-1">{errors.position.message}</p>}
            </div>
          </div>

          <div className="grid grid-cols-1 md:grid-cols-2 gap-4">
            <div>
              <Label htmlFor="hireDate">Hire Date</Label>
              <Input id="hireDate" type="date" {...register("hireDate")} className="mt-1" readOnly={isReadOnlyCoreFields} />
              {errors.hireDate && <p className="text-sm text-destructive mt-1">{errors.hireDate.message}</p>}
            </div>
            <div>
              <Label htmlFor="role">Role</Label>
              <Controller
                name="role"
                control={control}
                render={({ field }) => (
                  <Select
                    value={field.value}
                    onValueChange={field.onChange}
                    disabled={isRoleReadOnly}
                  >
                    <SelectTrigger className="mt-1">
                      <SelectValue placeholder="Select a role" />
                    </SelectTrigger>
                    <SelectContent>
                      {USER_ROLES.map((roleValue) => (
                        <SelectItem key={roleValue} value={roleValue}>
                          {roleValue.charAt(0) + roleValue.slice(1).toLowerCase()}
                        </SelectItem>
                      ))}
                    </SelectContent>
                  </Select>
                )}
              />
              {errors.role && <p className="text-sm text-destructive mt-1">{errors.role.message}</p>}
            </div>
          </div>

           <div>
              <Label htmlFor="supervisorId">Supervisor</Label>
              <Controller
                name="supervisorId"
                control={control}
                render={({ field }) => (
                  <Select
                    value={field.value || NO_SUPERVISOR_VALUE} // Handle empty string or undefined from field.value
                    onValueChange={(value) => {
                      field.onChange(value === NO_SUPERVISOR_VALUE ? "" : value);
                    }}
                    disabled={isReadOnlyCoreFields}
                  >
                    <SelectTrigger className="mt-1">
                      <SelectValue placeholder="Select a supervisor" />
                    </SelectTrigger>
                    <SelectContent>
                      <SelectItem value={NO_SUPERVISOR_VALUE}>No Supervisor</SelectItem>
                      {supervisors.map((sup) => (
                        <SelectItem key={sup.id} value={sup.id}>
                          {sup.name}
                        </SelectItem>
                      ))}
                    </SelectContent>
                  </Select>
                )}
              />
              {errors.supervisorId && <p className="text-sm text-destructive mt-1">{errors.supervisorId.message}</p>}
            </div>

          <div>
              <Label htmlFor="avatarUrl">Avatar URL (Optional)</Label>
              <Input id="avatarUrl" {...register("avatarUrl")} className="mt-1" placeholder="https://placehold.co/100x100.png" readOnly={isReadOnlyCoreFields && !canEditAllFields /* Allow self-edit of avatar */} />
              {errors.avatarUrl && <p className="text-sm text-destructive mt-1">{errors.avatarUrl.message}</p>}
          </div>

          <DialogFooter className="mt-4">
            <Button type="button" variant="outline" onClick={() => setIsOpen(false)}>
              Cancel
            </Button>
            <Button type="submit" disabled={isReadOnlyCoreFields && !canEditAllFields && !(employee && employee.avatarUrl !== control._formValues.avatarUrl) && isRoleReadOnly}>
                {employee ? "Save Changes" : "Add Employee"}
            </Button>
          </DialogFooter>
        </form>
      </DialogContent>
    </Dialog>
  );
}
